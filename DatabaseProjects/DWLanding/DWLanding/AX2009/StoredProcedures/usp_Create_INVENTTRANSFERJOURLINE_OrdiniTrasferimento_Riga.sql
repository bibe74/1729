	QTYSCRAPPED		NUMERIC(28, 12) NOT NULL

	-- Constraint
	CONSTRAINT PK_INVENTTRANSFERJOURLINE_OrdiniTrasferimento_Riga PRIMARY KEY CLUSTERED (
		DATAAREAID,
		TRANSFERID,
		VOUCHERID,
		LINENUM,
