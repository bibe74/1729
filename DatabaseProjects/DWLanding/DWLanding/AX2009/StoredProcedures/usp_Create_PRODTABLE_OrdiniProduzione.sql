    QTYSCHED        NUMERIC(28, 12) NOT NULL,
    NPOPRODQTYMORE        NUMERIC(28, 12) NOT NULL,
    REMAININVENTPHYSICAL        NUMERIC(28, 12) NOT NULL

    -- Constraint
    CONSTRAINT PK_PRODTABLE_OrdiniProduzione PRIMARY KEY CLUSTERED (
        DATAAREAID,
        PRODID
    )
);

END;
GO

