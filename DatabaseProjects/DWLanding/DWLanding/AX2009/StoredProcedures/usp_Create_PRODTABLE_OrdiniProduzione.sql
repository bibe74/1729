    SCHEDTOTIME        INT NOT NULL,
    SCHEDFROMTIME        INT NOT NULL,
    CREATEDDATETIME        DATETIME NOT NULL,
    NPOSEQUENCENUMBER        INT NOT NULL,
    NPOBOMPALLETID        NVARCHAR(18) NOT NULL,
    NPOSTUPTIME        INT NOT NULL,
    NPOFINISHINGID        NVARCHAR(512) NOT NULL,
    NPOLENGTH        NVARCHAR(512) NOT NULL,
    METLOCKPACKING        BIT NOT NULL,

