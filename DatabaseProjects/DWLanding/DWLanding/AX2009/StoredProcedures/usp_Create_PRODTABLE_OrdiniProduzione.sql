    -- Chiavi
    DATAAREAID        CHAR(4) NOT NULL,
    PRODID        NVARCHAR(20) NOT NULL,
    RECID        BIGINT NOT NULL,

    -- Dimensioni
    ITEMID        NVARCHAR(20) NOT NULL,
    PRODSTATUS        SMALLINT NOT NULL,
    SCHEDSTATUS        SMALLINT NOT NULL,
    DLVDATE        DATE NOT NULL,
    STUPDATE        DATE NOT NULL,
    FINISHEDDATE        DATE NOT NULL,
    SCHEDSTART        DATE NOT NULL,
    SCHEDEND        DATE NOT NULL,
    INVENTTRANSID        NVARCHAR(20) NOT NULL,
