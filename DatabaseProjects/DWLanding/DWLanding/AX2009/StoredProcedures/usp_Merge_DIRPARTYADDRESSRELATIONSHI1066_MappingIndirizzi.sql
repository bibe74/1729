WHEN NOT MATCHED
  THEN INSERT VALUES (DATAAREAID, PARTYADDRESSRELATIONSHIPRECID, ADDRESSRECID, REFCOMPANYID, RECID)
WHEN NOT MATCHED BY SOURCE
  THEN DELETE
OUTPUT
	CURRENT_TIMESTAMP AS merge_datetime,
	$action AS merge_action,
	'AX2009.DIRPARTYADDRESSRELATIONSHI1066_MappingIndirizzi' AS full_olap_table_name,
	'DATAAREAID = ' + CAST(COALESCE(inserted.DATAAREAID, deleted.DATAAREAID) AS NVARCHAR) + ' - ' + 'PARTYADDRESSRELATIONSHIPRECID = ' + CAST(COALESCE(inserted.PARTYADDRESSRELATIONSHIPRECID, deleted.PARTYADDRESSRELATIONSHIPRECID) AS NVARCHAR) + ' - ' + 'ADDRESSRECID = ' + CAST(COALESCE(inserted.ADDRESSRECID, deleted.ADDRESSRECID) AS NVARCHAR) + ' - ' + 'REFCOMPANYID = ' + CAST(COALESCE(inserted.REFCOMPANYID, deleted.REFCOMPANYID) AS NVARCHAR) AS primary_key_description
INTO audit.merge_log_details;

END;
GO

