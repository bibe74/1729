
CREATE PROCEDURE AX2009.usp_Merge_DIRPARTYADDRESSRELATIONSHI1066_MappingIndirizzi
AS
BEGIN
SET NOCOUNT ON;

IF OBJECT_ID(N'AX2009.DIRPARTYADDRESSRELATIONSHI1066_MappingIndirizzi', N'U') IS NULL
BEGIN
	EXEC AX2009.usp_Create_DIRPARTYADDRESSRELATIONSHI1066_MappingIndirizzi;
	EXEC AX2009.usp_AddIndexes_DIRPARTYADDRESSRELATIONSHI1066_MappingIndirizzi;
END;

MERGE INTO AX2009.DIRPARTYADDRESSRELATIONSHI1066_MappingIndirizzi AS TGT
USING AX2009.DIRPARTYADDRESSRELATIONSHI1066_MappingIndirizziView (nolock) AS SRC
ON SRC.DATAAREAID = TGT.DATAAREAID AND SRC.PARTYADDRESSRELATIONSHIPRECID = TGT.PARTYADDRESSRELATIONSHIPRECID AND SRC.ADDRESSRECID = TGT.ADDRESSRECID AND SRC.REFCOMPANYID = TGT.REFCOMPANYID
--WHEN MATCHED AND ()
