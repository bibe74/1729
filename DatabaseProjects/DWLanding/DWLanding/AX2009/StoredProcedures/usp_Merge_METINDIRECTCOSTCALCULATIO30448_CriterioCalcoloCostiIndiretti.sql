  THEN INSERT VALUES (DATAAREAID, SECTORTYPE, MNGTYPE, INVENTITEMTYPE, FROMDATE, FILTERCODE, CUSTACCOUNT, RECID, COSTCALCULATIONTYPE, PERCENT_)
WHEN NOT MATCHED BY SOURCE
  THEN DELETE
OUTPUT
