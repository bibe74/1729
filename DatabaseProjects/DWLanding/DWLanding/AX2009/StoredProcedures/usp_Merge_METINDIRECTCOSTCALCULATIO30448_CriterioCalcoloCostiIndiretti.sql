
CREATE PROCEDURE AX2009.usp_Merge_METINDIRECTCOSTCALCULATIO30448_CriterioCalcoloCostiIndiretti
AS
BEGIN
SET NOCOUNT ON;

IF OBJECT_ID(N'AX2009.METINDIRECTCOSTCALCULATIO30448_CriterioCalcoloCostiIndiretti', N'U') IS NULL
BEGIN
	EXEC AX2009.usp_Create_METINDIRECTCOSTCALCULATIO30448_CriterioCalcoloCostiIndiretti;
	EXEC AX2009.usp_AddIndexes_METINDIRECTCOSTCALCULATIO30448_CriterioCalcoloCostiIndiretti;
END;

MERGE INTO AX2009.METINDIRECTCOSTCALCULATIO30448_CriterioCalcoloCostiIndiretti AS TGT
USING AX2009.METINDIRECTCOSTCALCULATIO30448_CriterioCalcoloCostiIndirettiView (nolock) AS SRC
ON SRC.DATAAREAID = TGT.DATAAREAID AND SRC.SECTORTYPE = TGT.SECTORTYPE AND SRC.MNGTYPE = TGT.MNGTYPE AND SRC.INVENTITEMTYPE = TGT.INVENTITEMTYPE AND SRC.FROMDATE = TGT.FROMDATE AND SRC.FILTERCODE = TGT.FILTERCODE AND SRC.CUSTACCOUNT = TGT.CUSTACCOUNT
WHEN MATCHED AND (TGT.COSTCALCULATIONTYPE <> SRC.COSTCALCULATIONTYPE OR TGT.PERCENT_ <> SRC.PERCENT_)
  THEN UPDATE SET TGT.COSTCALCULATIONTYPE = SRC.COSTCALCULATIONTYPE, TGT.PERCENT_ = SRC.PERCENT_
